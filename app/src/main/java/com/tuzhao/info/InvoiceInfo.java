public class InvoiceInfo {

    private String id;

    private String parkLot;

    private String time;

    private String img;

    private String check;

    private String location;

    private String parkDuration;

    private String price;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getParkLot() {
        return parkLot;
    }

    public void setParkLot(String parkLot) {
        this.parkLot = parkLot;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getCheck() {
        return check;
    }

    public void setCheck(String check) {
        this.check = check;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getParkDuration() {
        return parkDuration;
    }

    public void setParkDuration(String parkDuration) {
        this.parkDuration = parkDuration;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}

public class CreditRecordInfo {

    private String credit;

    private String creditDiversification;

    private String updateDate;

    private String encourageWord;

    public String getCredit() {
        return credit;
    }

    public void setCredit(String credit) {
        this.credit = credit;
    }

    public String getCreditDiversification() {
        return creditDiversification;
    }

    public void setCreditDiversification(String creditDiversification) {
        this.creditDiversification = creditDiversification;
    }

    public String getUpdateDate() {
        return updateDate;
    }

    public void setUpdateDate(String updateDate) {
        this.updateDate = updateDate;
    }

    public String getEncourageWord() {
        return encourageWord;
    }

    public void setEncourageWord(String encourageWord) {
        this.encourageWord = encourageWord;
    }
}

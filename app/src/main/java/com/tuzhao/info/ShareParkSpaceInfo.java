public class ShareParkSpaceInfo {

    private String id;

    private String parkSpaceName;

    private String carOwnerName;

    private String parkSpaceStatus;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getParkSpaceName() {
        return parkSpaceName;
    }

    public void setParkSpaceName(String parkSpaceName) {
        this.parkSpaceName = parkSpaceName;
    }

    public String getCarOwnerName() {
        return carOwnerName;
    }

    public void setCarOwnerName(String carOwnerName) {
        this.carOwnerName = carOwnerName;
    }

    public String getParkSpaceStatus() {
        return parkSpaceStatus;
    }

    public void setParkSpaceStatus(String parkSpaceStatus) {
        this.parkSpaceStatus = parkSpaceStatus;
    }
}
